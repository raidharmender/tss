/*
 * Complete the 'containsEveryVowel' function below.
 *
 * The function is expected to return a BOOLEAN.
 * The function accepts STRING str as parameter.
 */

function containsEveryVowel(str) {     
    return str.includes("a")
    && str.includes("e")
    && str.includes("i")
    && str.includes("o")
    && str.includes("u")
}